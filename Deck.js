export class Card {
	constructor(japanese, english) {
		this.japanese = japanese;
		this.english = english;
	}
}

export class Deck {
	constructor(deckName) {
		this.deckName = deckName;
		this.cards = [];
		this.currJap = 'empty';
		this.currEn = 'empty';
		this.index = 0;
	}

	addCard(newCard) {
		if (this.cards.length === 0) {
			this.currJap = newCard.japanese;
			this.currEn = newCard.english;
		}
		this.cards.push(newCard);
	}

	next() {
		this.index = (this.index+1)%this.cards.length;
		this.currJap = this.cards[this.index].japanese;
		this.currEn = this.cards[this.index].english;
	}
}

export class DeckTile {
	constructor(deck, func) {
		this.onclick = this.onclick.bind(this);
		this.deckName = deck.deckName;
		this.deck = deck;
		this.emoji = emojis[Math.floor(Math.random() * emojis.length)];
		const elem = document.createElement('div');
		elem.className = 'deck-tile';
		const elem2 = document.createElement('div');
		elem2.className = 'deck-tile-image';
		const elem3 = document.createElement('div');
		elem3.className = 'emoji-wrapper';
		elem3.textContent = this.emoji;
		elem2.appendChild(elem3);
		const text = document.createElement('p');
		text.textContent = this.deckName;
		text.className = 'deck-tile-text'
		elem2.style.backgroundColor = `rgb(${parseInt(Math.random()*256)}, ${parseInt(Math.random()*256)}, ${parseInt(Math.random()*256)}`;
		elem.appendChild(elem2);
		elem.appendChild(text);
		elem.addEventListener('click', this.onclick);
		this.elem = elem;
		this.func = func;
	}

	onclick() {
		this.func(this.deck);
	}
}

